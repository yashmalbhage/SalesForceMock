import mongoose from 'mongoose'

const scheduleSchema = mongoose.Schema({
  userId: {
    type: String,
    required: true,
  },

  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  phoneNo: {
    type: String,
    required: true,
  },
  experience: {
    type: String,
    required: true,
  },
  date: {
    type: String,
    required: true,
  },
  time: {
    type: String,
    required: true,
  },
})

// Use 'Schedule' directly instead of assigning it to mongoose.models
const Schedule = mongoose.model('Schedule', scheduleSchema)

export default Schedule
